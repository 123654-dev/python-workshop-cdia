# Zuweisung von Variablen

# Variablen sind Platzhalter für Werte.
# Der Variablenname steht bei der Zuweisung links vom Gleichheitszeichen und der Wert analog dazu rechts.
meine_variable = 20

# Variablen können beliebige Namen haben, müssen aber mit einem Buchstaben beginnen.
# In Python können sie beliebige Werte enthalten:

x = 1
name = "Umberto"
wahr = True
nicht_wahr = False
ich_kann_hier_fast_jeden_beliebigen_namen_verwenden = 420

# Der in den Variablen gespeicherte Wert kann jederzeit abgerufen und geändert werden:
print(x) # Ausgabe: 1
x = 2
print(x) # Ausgabe: 2

# Ausgabe der Variablen
print(x)
print(name)
print(wahr)
print(nicht_wahr)
print(ich_kann_hier_fast_jeden_beliebigen_namen_verwenden)
